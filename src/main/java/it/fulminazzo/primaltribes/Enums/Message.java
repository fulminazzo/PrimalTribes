    public static final String ERROR = ChatColor.translateAlternateColorCodes('&', "&4ERROR");

    protected final String path;
    
    public Message(String path) {
        this.path = path;
    }

    public String getMessage(boolean normalPrefix, boolean adminPrefix) {
        String message = PrimalTribes.getPlugin().getLang().getString(path);
        MessageLookupEvent messageLookupEvent = new MessageLookupEvent(path, normalPrefix, adminPrefix, message);
        message = messageLookupEvent.getMessage();
        normalPrefix = messageLookupEvent.isNormalPrefix();
        adminPrefix = messageLookupEvent.isAdminPrefix();
        if (message == null) message = ERROR;
        message = ChatColor.translateAlternateColorCodes('&', message);
        String commandName = PrimalTribes.getCommandName();
        if (message.replace(" ", "").equalsIgnoreCase("")) return "";
        if (normalPrefix) message = Message.PREFIX.getMessage(false, false) + message;
        if (adminPrefix) message = Message.ADMIN_PREFIX.getMessage(false, false) + message;
        if (commandName != null) message = message.replace("%command-name%", commandName);
        return HexUtils.parseString(message);
    }
